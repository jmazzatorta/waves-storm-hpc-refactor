def get_stats(data_arrays):
    """Unisce i batch, calcola mediana, min e max."""
    merged = np.concatenate(data_arrays)
    med = np.median(merged)
    return med, np.min(merged), np.max(merged)


def get_weak_stats(data_arrays):
    """Come get_stats, ma restituisce gli offset per errorbar [err_low, err_high]."""
    med, p_min, p_max = get_stats(data_arrays)
    return med, [med - p_min, p_max - med]


def calc_speedup(base_med, par_stats):
    """Calcola lo speedup mediano e le barre di errore."""
    par_med, par_min, par_max = par_stats
    s_med = base_med / par_med
    s_max = base_med / par_min
    s_min = base_med / par_max
    return s_med, [s_med - s_min, s_max - s_med]


